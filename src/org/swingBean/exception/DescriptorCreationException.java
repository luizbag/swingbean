package org.swingBean.exception;

public class DescriptorCreationException extends RuntimeException{

	public DescriptorCreationException() {
		super();
	}

	public DescriptorCreationException(String message, Throwable cause) {
		super(message, cause);
	}

	public DescriptorCreationException(String message) {
		super(message);
	}

	public DescriptorCreationException(Throwable cause) {
		super(cause);
	}	
	
}
